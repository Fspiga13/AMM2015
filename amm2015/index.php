        <h2> Descrizione dell'applicazione </h2>
        <p>
            L’applicazione supporta la prenotazione di viaggi in localit&agrave surfistiche. <br>
            La funzionalità di base prevede che un organizzatore possa inserire i dati <br>
            relativi a un viaggio prenotabile da un utente. <br>
            I dati che figurano per ogni viaggio sono i seguenti:<br>
        </p>
        <ul>
            <li>Nazione e Citt&agrave di destinazione</li>
            <li>Data di partenza e rientro del viaggio</li>
            <li>Prezzo del viaggio</li>
            <li>Capienza del viaggio</li>
        </ul>


        <p>Inoltre, l'utente &egrave; in grado di visualizzare <br>
            le sue prenotazioni e i viaggi disponibili sul web.<br>
        </p>

        <p>L’applicazione mantiene una anagrafica degli organizzatori e degli utenti, in particolare:</p>

        <ul>
            <li>Nome e Cognome</li>
            <li>Indirizzo</li>
            <li>Email</li>
        </ul>

            Inoltre, l’applicazione fornisce istruzioni dettagliate sulle modalit&agrave; <br>
	    di inserimento dei dati personali (che può essere fatto direttamente da ogni <br>
	    utente) e sulla visualizzazione dei viaggi nelle varie sezioni attinenti.
        </p>
        <p>
            L’applicazione gestisce la prenotazione dei viaggi da parte degli utenti: <br>
            l'organizzatore inserisce i dati come prima indicato e l'utente si connette <br>
	    e si prenota nel caso ci siano ancora posti disponibili. 
        </p>

        <h2> Requisiti del progetto </h2>
        <ul>
            <li>Utilizzo di HTML e CSS</li>
            <li>Utilizzo di PHP e MySQL</li>
            <li>Utilizzo del pattern MVC </li>
            <li>Due ruoli (utente e organizzatore)</li>
        </ul>
    </ul>

